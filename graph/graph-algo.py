# Graphs: 

# Graphs (connected, fully connected, bi-partite)

# A Graph is a non-linear data structure consisting of nodes and edges. nodes are also known as vertex-vertices(multiple).

# Undirected graphs have edges that do not have a direction. The edges indicate a two-way relationship, in that each edge can be traversed in both directions. The undirected graph contains edges.

# Directed graphs have edges with direction. The edges indicate a one-way relationship, in that each edge can only be traversed in a single direction. The directed graph contains arcs.  

# Weighted graphs - Graphs can sometimes have weights. The weights not necessarily, but usually denote the distance between two vertices. The weighted graphs can be both directed and undirected.

# Cyclic graphs are graphs that form a cycle. The cyclic graphs can be directed, undirected or weighted. So usually when we traverse through a graph, we mark the vertices which are already visited.

# graphs are easily built out of lists and dictionaries.
# keys are the nodes of the graph
# For each key, the corresponding value is a list containing the nodes that are connected by a direct arc from this node

